document.addEventListener('DOMContentLoaded', function() {
  // ===== HEADER SCROLL EFFECT =====
  const header = document.getElementById('header');
  let lastScrollY = 0;

  function handleScroll() {
    const scrollY = window.scrollY;
    
    if (scrollY > 20) {
      header.classList.add('scrolled');
    } else {
      header.classList.remove('scrolled');
    }
    
    lastScrollY = scrollY;
  }

  window.addEventListener('scroll', handleScroll);
  handleScroll(); // Check initial state

  // ===== MOBILE MENU TOGGLE =====
  const mobileMenuBtn = document.getElementById('mobileMenuBtn');
  const mobileNav = document.getElementById('mobileNav');
  const menuIconOpen = document.getElementById('menuIconOpen');
  const menuIconClose = document.getElementById('menuIconClose');

  function toggleMobileMenu() {
    const isOpen = mobileNav.classList.toggle('active');
    menuIconOpen.style.display = isOpen ? 'none' : 'block';
    menuIconClose.style.display = isOpen ? 'block' : 'none';
  }

  mobileMenuBtn.addEventListener('click', toggleMobileMenu);

  // Close mobile menu when clicking a link
  const mobileNavLinks = mobileNav.querySelectorAll('a');
  mobileNavLinks.forEach(link => {
    link.addEventListener('click', () => {
      mobileNav.classList.remove('active');
      menuIconOpen.style.display = 'block';
      menuIconClose.style.display = 'none';
    });
  });

  // ===== SMOOTH SCROLL FOR ANCHOR LINKS =====
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function(e) {
      e.preventDefault();
      const targetId = this.getAttribute('href');
      const targetElement = document.querySelector(targetId);
      
      if (targetElement) {
        const headerHeight = header.offsetHeight;
        const targetPosition = targetElement.offsetTop - headerHeight;
        
        window.scrollTo({
          top: targetPosition,
          behavior: 'smooth'
        });
      }
    });
  });

  // ===== SCROLL TO TOP BUTTON =====
  const scrollTopBtn = document.getElementById('scrollTop');

  function toggleScrollTopButton() {
    if (window.scrollY > 500) {
      scrollTopBtn.classList.add('visible');
    } else {
      scrollTopBtn.classList.remove('visible');
    }
  }

  window.addEventListener('scroll', toggleScrollTopButton);

  scrollTopBtn.addEventListener('click', () => {
    window.scrollTo({
      top: 0,
      behavior: 'smooth'
    });
  });

  // ===== CONTACT FORM HANDLING =====
  const contactForm = document.getElementById('contactForm');

  if (contactForm) {
    contactForm.addEventListener('submit', function(e) {
      e.preventDefault();
      
      // Get form data
      const formData = new FormData(contactForm);
      const data = {
        nom: formData.get('nom'),
        email: formData.get('email'),
        sujet: formData.get('sujet'),
        message: formData.get('message')
      };

      // Simulate form submission (replace with actual API call)
      console.log('Form submitted:', data);
      
      // Show success message
      alert('Message envoyé avec succès ! Nous vous répondrons dans les plus brefs délais.');
      
      // Reset form
      contactForm.reset();
    });
  }

  // ===== RENDER DYNAMIC DATA =====
  // This function can be used to render data from data.js if needed
  function renderData() {
    // Stats
    const statsContainer = document.getElementById('statsContainer');
    if (statsContainer && typeof DATA !== 'undefined') {
      // Data is already rendered in EJS, but this can be used for dynamic updates
    }

    // UGF List
    const ugfContainer = document.getElementById('ugfList');
    if (ugfContainer && typeof DATA !== 'undefined') {
      ugfContainer.innerHTML = DATA.ugf.map(ugf => `
        <div class="ugf-item">
          <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
            <path d="M17 14V2"/>
            <path d="M9 18.12 10 14H4.17a2 2 0 0 1-1.92-2.56l2.33-8A2 2 0 0 1 6.5 2H20a2 2 0 0 1 2 2v8a2 2 0 0 1-2 2h-2.76a2 2 0 0 0-1.79 1.11L12 22h0a3.13 3.13 0 0 1-3-3.88Z"/>
          </svg>
          ${ugf.nom}
        </div>
      `).join('');
    }
  }

  // Call render if needed
  // renderData();

  // ===== INTERSECTION OBSERVER FOR ANIMATIONS =====
  const observerOptions = {
    root: null,
    rootMargin: '0px',
    threshold: 0.1
  };

  const animateOnScroll = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('animate-in');
        animateOnScroll.unobserve(entry.target);
      }
    });
  }, observerOptions);

  // Observe elements with data-animate attribute
  document.querySelectorAll('[data-animate]').forEach(el => {
    animateOnScroll.observe(el);
  });

  // ===== YEAR IN FOOTER =====
  const yearElement = document.getElementById('currentYear');
  if (yearElement) {
    yearElement.textContent = new Date().getFullYear();
  }

  // ===== IMAGE LAZY LOADING =====
  const images = document.querySelectorAll('img[data-src]');
  
  const imageObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const img = entry.target;
        img.src = img.dataset.src;
        img.removeAttribute('data-src');
        observer.unobserve(img);
      }
    });
  });

  images.forEach(img => imageObserver.observe(img));

  // ===== CONSOLE LOG =====
  console.log('%c GESTREB - Centre de Gestion de Gagnoa ', 
    'background: #059669; color: white; font-size: 14px; padding: 10px; border-radius: 5px;');
  console.log('Page d\'accueil chargée avec succès');
});

